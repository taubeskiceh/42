#include <unistd.h>

void	ft_print_comb2(void)
{
	int		a;
	int		b;
	char	buf[5];

	a = 0;
	while (a <= 98)
	{
		b = a + 1;
		while (b <= 99)
		{
			buf[0] = (char)('0' + (a / 10));
			buf[1] = (char)('0' + (a % 10));
			buf[2] = ' ';
			buf[3] = (char)('0' + (b / 10));
			buf[4] = (char)('0' + (b % 10));
			write(1, buf, 5);
			if (!(a == 98 && b == 99))
				write(1, ", ", 2);
			b++;
		}
		a++;
	}
}
